def generate_report(date_str=None):
    """生成完整的早盘分析报告"""
    if date_str is None:
        date_str = datetime.now().strftime('%Y%m%d')

    report_title = f"# {date_str[:4]}-{date_str[4:6]}-{date_str[6:]} A股早盘分析报告\n"
    report_time = f"\n> 报告生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n---\n"

    premarket_news = generate_premarket_news()
    trading_strategy = generate_trading_strategy()
    portfolio_analysis = generate_portfolio_analysis()

    full_report = report_title + report_time + premarket_news + "\n\n---\n" + \
                  trading_strategy + "\n\n---\n" + portfolio_analysis + "\n\n---\n\n" + \
                  "**免责声明**：本报告仅供参考，不构成投资建议。股市有风险，投资需谨慎。\n"

    return full_report


def save_report(report, date_str=None):
    """保存报告到文件"""
    if date_str is None:
        date_str = datetime.now().strftime('%Y%m%d')

    log_dir = "/workspace/logs"
    if not os.path.exists(log_dir):
        os.makedirs(log_dir)

    filename = os.path.join(log_dir, f"早盘分析_{date_str}.md")
    with open(filename, "w", encoding="utf-8") as f:
        f.write(report)

    print(f"报告已保存至: {filename}")
    return filename


def push_report(title, report_path):
    """推送报告"""
    # 尝试调用notify_lite.py，如果不存在则打印报告内容
    notify_script = "/workspace/notify_lite.py"
    if os.path.exists(notify_script):
        try:
            import subprocess
            result = subprocess.run(
                [sys.executable, notify_script, title, report_path],
                capture_output=True,
                text=True,
                check=True
            )
            print("报告推送成功！")
            print(result.stdout)
        except Exception as e:
            print(f"推送失败: {e}")
            print("\n报告内容预览：")
            print("="*60)
            with open(report_path, "r", encoding="utf-8") as f:
                print(f.read())
    else:
        print(f"未找到推送脚本: {notify_script}")
        print("\n报告内容预览：")
        print("="*60)
        with open(report_path, "r", encoding="utf-8") as f:
            print(f.read())


def main():
    """主函数"""
    date_str = "20260507"  # 今日日期
    print(f"开始生成{date_str}的早盘分析报告...\n")

    # 生成报告
    report = generate_report(date_str)

    # 保存报告
    report_path = save_report(report, date_str)

    # 推送报告
    push_report("早盘分析", report_path)

    print("\n早盘分析完成！")


if __name__ == "__main__":
    main()
